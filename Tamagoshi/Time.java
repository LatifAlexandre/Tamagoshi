import java.lang.String;

public class Time
{
     private date time;
     private String season;
     private boolean dayTime; // 0 for night and 1 for day
     
     
     /*************************************\
     /       Setters an Getters            /
     \*************************************/
     
     public date getTime()
     {
          return time;
     }
     
     public String getSeason()
     {
          return season;
     }
     
     public boolean getDayTime()
     {
          return dayTime;
     }











}
